    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = txtGhiChu.getText();
        if (name == null || "".equals(name.trim())) {
      
            showMessage("Ghi chú không được trống.");
        }else{
        try {
            String request = "insert into DatPhong(id_user, MaKH, MaPhong, NgayDat, GhiChu, TinhTrang) VALUES (?,?,?,?,?,?);";
            ps = connection.prepareStatement(request);
            ps.setString(1,((Role) cbxMaNhanVien.getSelectedItem()).getMaQuyen()+"");
            ps.setString(2,((Role) cbxMaKH.getSelectedItem()).getMaQuyen()+"");
            ps.setString(3,((Role) cbxTenPhong.getSelectedItem()).getMaQuyen()+"");
             ps.setString(4, ((JTextField)(txtDate.getDateEditor().getUiComponent())).getText());
            ps.setString(5,txtGhiChu.getText());
            ps.setString(6,txtTinhTrang.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null, "saved");
        } catch (Exception e) {
             showMessage("Loi nhập dữ liệu");
        } finally {
            try {
                ps.close();
                rs.close();
            } catch (Exception e) {

            }
        }
        String b= ((Role) cbxTenPhong.getSelectedItem()).getMaQuyen()+"";
        String sql ="UPDATE Phong SET TinhTrang = N'Phòng bận' WHERE MaPhong = "+b+"";
        new MySqlConnectionUtil().thucThiCauLenhSQL(sql);
        this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

