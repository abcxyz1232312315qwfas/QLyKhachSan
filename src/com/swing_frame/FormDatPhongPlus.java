    public void loadCBBIdKhachHang() {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        rs = new MySqlConnectionUtil().chonDuLieuTuDTB("select * from KhachHang");
        try {
            while (rs.next()) {
                Role u = new Role();
                u.setMaQuyen(rs.getString(1));
                u.setTenQuyen(rs.getString(2));
                model.addElement(new Role(u.getMaQuyen(), u.getTenQuyen()));
                cbxMaKH.setModel(model);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
